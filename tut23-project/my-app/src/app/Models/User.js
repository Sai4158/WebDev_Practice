import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// look for this if not found create it
const User = mongoose.models.user || mongoose.model("user", UserSchema);
export default User;
